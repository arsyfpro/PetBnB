    private void ShowTable() {
        try{
            String sql = "SELECT p.status, p.id_pesanan, u.nama, p.nama_hewan, p.jenis, p.umur, p.makanan_fav_hewan, p.catatan, p.voucher, p.layanan, p.check_in, p.check_out, p.total, p.total_bayar FROM user u JOIN pesanan p ON p.id_user = u.id WHERE p.id_host = '"+id_host+"'";
            pstmt = cn.prepareStatement(sql);
            rs = pstmt.executeQuery();
            
            DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
            
            String[] data = new String[14];
            
            while(rs.next()){
                data[0] = rs.getString("p.id_pesanan");
                data[1] = rs.getString("u.nama");
                data[2] = rs.getString("p.nama_hewan");
                data[3] = rs.getString("p.jenis");
                data[4] = rs.getString("p.umur");
                data[5] = rs.getString("p.makanan_fav_hewan");
                data[6] = rs.getString("p.catatan");
                data[7] = rs.getString("p.voucher");
                data[8] = rs.getString("p.layanan");
                data[9] = rs.getString("p.check_in");
                data[10] = rs.getString("p.check_out");
                data[11] = rs.getString("p.total");
                data[12] = rs.getString("p.total_bayar");
                data[13] = rs.getString("p.status");
                dtm.addRow(data);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

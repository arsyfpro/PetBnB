    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        String id_p = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String stts = (String) pilUbah.getSelectedItem();
        String sql = "UPDATE pesanan SET status='"+stts+"' WHERE id_pesanan='"+id_p+"'";
        
        try {
            Statement st = cn.createStatement();
            st.addBatch(sql);
                    
            st.executeBatch();
            
            JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
            new RiwayatPesananHost(id_host).setVisible(true);
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(RiwayatPesananHost.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

